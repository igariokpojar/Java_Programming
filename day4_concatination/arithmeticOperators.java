package day4_concatination;

public class arithmeticOperators {
    public static void main(String[] args) {
        System.out.println(100+200); //300
        System.out.println(40-10);// 30
        System.out.println(50/6);// double result
        System.out.println(50/6.0);// double result
        System.out.println(50d/6);// decimal  number
        System.out.println(5*10);

        System.out.println(100%23);/// remainder is 8

        System.out.println(45%8);/// remainder is 5
    }





}
