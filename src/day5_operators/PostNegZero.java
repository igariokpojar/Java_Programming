package day5_operators;

public class PostNegZero {
    public static void main(String[] args) {
        int number = 20;

        if (number>0) {
            System.out.println("positive");
        }
        if (number<0) {
            System.out.println( "negative");
        }
        if (number==0) {
            System.out.println( "equal");
        }
    }

}
